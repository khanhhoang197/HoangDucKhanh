let inputwidth;
let inputheight;
inputwidth = prompt('Nhập chiều rộng của hình chữ nhật:');
inputheight = prompt('Nhập chiều rộng của hình chữ nhật: ');
let width = parseInt(inputwidth);
let height = parseInt(inputheight);
let area = width * height;
document.write('Diện tích của hình chữ nhật là: ' + area);